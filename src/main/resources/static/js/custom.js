function hideSideBar()
  {
    $("#sideMenuArrow").css('display','block');
      $("#sideMenuArrow").css('visibility','visible');
    $("#sidebarMenu").css('display','none');
      $("#sidebarMenu").css('visibility','hidden');
      $("#sidebarMenu").removeClass('d-md-block');
      $("#sidebarMenu").removeClass('bg-dark');
      $("#sidebarMenu").removeClass('col-md-3');
      $("#sidebarMenu").removeClass('col-lg-2');
      $("#mainContent").removeClass('col-md-9');
      $("#mainContent").removeClass('col-lg-10');
      $("#mainContent").addClass('col-md-12');
      $("#mainContent").addClass('col-lg-12');
  }
  function showSideBar()
    {
      $("#sideMenuArrow").css('display','none');
      $("#sideMenuArrow").css('visibility','hidden');
    $("#sidebarMenu").css('display','block');
        $("#sidebarMenu").css('visibility','visible');
        $("#sidebarMenu").addClass('d-md-block');
        $("#sidebarMenu").addClass('bg-dark');
        $("#sidebarMenu").addClass('col-md-3');
        $("#sidebarMenu").addClass('col-lg-2');
        $("#mainContent").addClass('col-md-9');
        $("#mainContent").addClass('col-lg-10');
        $("#mainContent").removeClass('col-md-12');
        $("#mainContent").removeClass('col-lg-12');
    }